use std::{f32, i16, rc::Rc, usize};

use crate::token::TokenKind;
use super::grammar::NotTerm;

#[derive(Debug, Clone, Copy)]
pub enum Action {
    Reduce(usize),
    Shift(usize),
    Accept,
}

impl NotTerm {
    const AXION: Self = Self::PP;
    const COUNT: usize = Self::AXION as usize;

    pub(super) fn idx(&self) -> usize {
        *self as usize
    }
}

impl TokenKind {
    const COUNT: usize = TokenKind::Eof.idx() + 1;

    pub(super) fn from_idx(idx: usize) -> Self {
        match idx {
            0 => TokenKind::If, 
            1 => TokenKind::Do,
            2 => TokenKind::While,
            3 => TokenKind::Int,
            4 => TokenKind::Float,
            5 => TokenKind::Str,
            6 => TokenKind::Bool,
            7 => TokenKind::Void,
            8 => TokenKind::Let,
            9 => TokenKind::Func,
            10 => TokenKind::Ret,
            11 => TokenKind::Read,
            12 => TokenKind::Write,
            13 => TokenKind::True,
            14 => TokenKind::False,
            15 => TokenKind::FloatLit(f32::default()),
            16 => TokenKind::IntLit(i16::default()),
            17 => TokenKind::StrLit(String::default()),
            18 => TokenKind::Id((usize::default(), Rc::from(""))),
            19 => TokenKind::Assign,
            20 => TokenKind::AndAssign,
            21 => TokenKind::Comma,
            22 => TokenKind::Semi,
            23 => TokenKind::LParen,
            24 => TokenKind::RParen,
            25 => TokenKind::LBrack,
            26 => TokenKind::RBrack,
            27 => TokenKind::Sum,
            28 => TokenKind::Sub,
            29 => TokenKind::Mul,
            30 => TokenKind::And,
            31 => TokenKind::Not,
            32 => TokenKind::Lt,
            33 => TokenKind::Eq,
            34 => TokenKind::Eof,
            other => unreachable!("should always be a valid TokenKind index, but got {other}"),
        }
    }

    pub(super) const fn idx(&self) -> usize {
        match self {
            TokenKind::If => 0, 
            TokenKind::Do => 1,
            TokenKind::While => 2,
            TokenKind::Int => 3,
            TokenKind::Float => 4,
            TokenKind::Str => 5,
            TokenKind::Bool => 6,
            TokenKind::Void => 7,
            TokenKind::Let => 8,
            TokenKind::Func => 9,
            TokenKind::Ret => 10,
            TokenKind::Read => 11,
            TokenKind::Write => 12,
            TokenKind::True => 13,
            TokenKind::False => 14,
            TokenKind::FloatLit(_) => 15,
            TokenKind::IntLit(_) => 16,
            TokenKind::StrLit(_) => 17,
            TokenKind::Id(_) => 18,
            TokenKind::Assign => 19,
            TokenKind::AndAssign => 20,
            TokenKind::Comma => 21,
            TokenKind::Semi => 22,
            TokenKind::LParen => 23,
            TokenKind::RParen => 24,
            TokenKind::LBrack => 25,
            TokenKind::RBrack => 26,
            TokenKind::Sum => 27,
            TokenKind::Sub => 28,
            TokenKind::Mul => 29,
            TokenKind::And => 30,
            TokenKind::Not => 31,
            TokenKind::Lt => 32,
            TokenKind::Eq => 33,
            TokenKind::Eof => 34,
        }
    }
}

